// 2. Data Types in Java
//    Primitive Data Types:
//    - int: Integer values (e.g., 17, -5, 0)
//    - float: Floating-point numbers with 'f' suffix (e.g., 17.10f)
//    - double: Double-precision floating-point with 'd' suffix (e.g., 17.1012d)
//    - boolean: true or false
//    - char: Single character in single quotes (e.g., 'a', 'A')
//    - byte, short, long: Other integer types with different ranges

// 3. User Input Handling
//    - Scanner class for reading user input
//    - Import statement: import java.util.Scanner;
//    - Creating Scanner object: Scanner sc = new Scanner(System.in);
//    - Input methods:
//      * nextInt() - reads integer
//      * nextFloat() - reads float
//      * nextDouble() - reads double
//      * next() - reads single word
//      * nextLine() - reads entire line
//      * nextBoolean() - reads boolean

