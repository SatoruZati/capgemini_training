package capgemini_20012026;

// Multilevel inheritance 

// when a class is derived from a class which is also derived from another class
// it is known as multilevel inheritance

// Extends and Implements 
// extends is used for inheriting a class
// implements is used for inheriting an interface

// Example of extends: 
// class Animal {
// 	void eat() {
// 		System.out.println("eating...");
// 	}
// }
// class Dog extends Animal {
// 	void bark() {
// 		System.out.println("barking...");
// 	}
// }

// Example of implements:
// interface Animal {
// 	void eat();
// }
// class Dog implements Animal {
// 	public void eat() {
// 		System.out.println("eating...");
// 	}
// 	void bark() {
// 		System.out.println("barking...");
// 	}
// }
//

// Multiple Inheritance is not supported in Java using classes
// But multiple inheritance is supported using interfaces
// and multi level inheritance is supported using classes

