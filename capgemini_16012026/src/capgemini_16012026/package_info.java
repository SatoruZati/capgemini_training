public class package_info {

	public static void main(String[] args) {
		// What is a package in Java?
		// A package is a namespace that organizes a set of related classes and interfaces.
		// Concept of package is similar to folder or directory in file system.
		
		// Why use packages?
		// 1. To avoid name conflicts
		// 2. To control access
		// 3. To make searching/locating and usage of classes, interfaces, enumerations and annotations easier
		// 4. To provide a hierarchical structure
		// 5. To organize files within a project
		
		System.out.println("This is a package info example.");
		// This is a package info example.
		
		// importing packages
		// import java.util.*; // imports all classes from java.util package
		// import package_name.class_name; // imports specific class from a package
		// import package_name.*; // imports all classes from a specific package
		// access specifiers with packages
		// 1. public: accessible from any other class
		// 2. protected: accessible within the same package and subclasses
		// 3. default (no specifier): accessible only within the same package
		// 4. private: accessible only within the same class
		
		
		

	}

}
